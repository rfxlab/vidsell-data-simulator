package com.rfxlab.vidsell.model;

public class GeoLocation {

	String LocID;
	String LocName;
	String ConChar;
	
	public String getLocID() {
		return LocID;
	}
	public void setLocID(String locID) {
		LocID = locID;
	}
	public String getLocName() {
		return LocName;
	}
	public void setLocName(String locName) {
		LocName = locName;
	}
	public String getConChar() {
		return ConChar;
	}
	public void setConChar(String conChar) {
		ConChar = conChar;
	}
	
	
}
